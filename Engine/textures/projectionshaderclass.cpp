	bool result;

	D3DXMATRIX viewMatrix2, projectionMatrix2;

	m_pointView->GetViewMatrix(viewMatrix2);
	m_pointView->GetProjectionMatrix(projectionMatrix2);

	// Set the shader parameters that it will use for rendering.
